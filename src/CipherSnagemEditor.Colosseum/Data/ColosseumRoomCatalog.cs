    public static IReadOnlyList<ColosseumNamedResource> AllRooms
        => NamesById
            .OrderBy(room => room.Value, StringComparer.OrdinalIgnoreCase)
            .Select(room => new ColosseumNamedResource(room.Key, room.Value))
            .ToArray();

    public static string NameFor(int id)
        => id == 0
            ? "-"
            : NamesById.TryGetValue(id, out var name)
                ? name
                : $"Room 0x{id:X4}";

    public static string MapNameForRoom(string roomName)
    {
        var prefix = Prefix(roomName);
        return prefix switch
        {
            "B1" => "Demo Area",
            "D1" => "Shadow Pokemon Lab",
            "D2" => "Mt. Battle",
            "D3" => "S.S. Libra",
            "D4" => "Realgam Tower",
