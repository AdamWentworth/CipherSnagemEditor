            "M5" => "Pokemon HQ Lab",
            "M6" => "Gateon Port",
            "S1" => "Outskirt Stand",
            "S2" => "Team Snagem's Hideout",
            "S3" => "Kaminko's House",
            "T1" => "Ancient Colosseum",
            "es" => "Pokespot",
            _ => string.Empty
        };
    }

    public static string Prefix(string roomName)
    {
        if (string.IsNullOrWhiteSpace(roomName) || roomName == "-")
        {
            return string.Empty;
        }

        var separator = roomName.IndexOf('_', StringComparison.Ordinal);
        return separator <= 0 ? roomName : roomName[..separator];
    }
